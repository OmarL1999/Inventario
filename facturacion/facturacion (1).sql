        DECLARE ventas int;
        
        	SELECT COUNT(*) INTO usuarios FROM usuario WHERE estatus != 10;
        	SELECT COUNT(*) INTO clientes FROM cliente WHERE estatus != 10;
          	SELECT COUNT(*) INTO proveedores FROM proveedor WHERE estatus != 10;
